      <h2>Alimentos Permitidos</h2>
      <ul>
        <li><strong>Gorduras saudáveis:</strong> azeite de oliva extravirgem, óleo de coco, manteiga, abacate</li>
        <li><strong>Proteínas:</strong> carnes bovinas, frango, peixes (especialmente peixes gordurosos), ovos</li>
        <li><strong>Laticínios:</strong> queijos gordos, creme de leite, iogurte grego integral</li>
        <li><strong>Vegetais de baixo carboidrato:</strong> folhas verdes, brócolis, couve-flor, abobrinha, espinafre</li>
        <li><strong>Oleaginosas:</strong> nozes, amêndoas, macadâmia, pecã</li>
        <li><strong>Frutos do mar:</strong> camarão, lagosta, ostras, sardinha</li>
      </ul>

      <h2>Alimentos Proibidos</h2>
      <ul>
        <li>Pão, massas, arroz, cereais e farinhas em geral</li>
        <li>Açúcar, mel, xarope de milho e adoçantes calóricos</li>
        <li>Frutas com alto teor de açúcar (banana, manga, uva, melancia)</li>
        <li>Leguminosas: feijão, grão-de-bico, lentilha</li>
        <li>Tubérculos: batata, mandioca, inhame, beterraba</li>
        <li>Bebidas adoçadas, sucos industrializados e álcool em excesso</li>
      </ul>

