      <h2>Benefícios Documentados</h2>
      <p>Diversos estudos clínicos e revisões sistemáticas têm demonstrado os seguintes benefícios para indivíduos saudáveis que adotam a dieta cetogênica com supervisão profissional:</p>
      <ul>
        <li><strong>Perda de peso acelerada</strong> nas primeiras semanas, inicialmente por eliminação de líquidos e depois por queima de gordura</li>
        <li><strong>Redução da glicemia de jejum</strong> e melhora da resistência insulínica em pacientes com diabetes tipo 2</li>
        <li><strong>Redução dos triglicerídeos</strong> e aumento do HDL ("colesterol bom")</li>
        <li><strong>Controle do apetite</strong> — os corpos cetônicos suprimem naturalmente a fome</li>
        <li><strong>Melhora do foco e clareza mental</strong> relatada por grande parte dos praticantes após a adaptação</li>
        <li><strong>Redução da inflamação sistêmica</strong> de acordo com marcadores laboratoriais</li>
      </ul>

      <div class="highlight">
        ⚠️ <strong>Atenção:</strong> Antes de iniciar qualquer protocolo cetogênico, consulte um médico ou nutricionista. A dieta pode ser contraindicada em casos de pancreatite, insuficiência hepática, distúrbios do metabolismo de gorduras e outros.
      </div>

      <h2>Efeitos Colaterais — "Keto Flu"</h2>
      <p>Nas primeiras 1–2 semanas de adaptação, é comum experimentar a chamada "gripe cetogênica" (keto flu): fadiga, dor de cabeça, irritabilidade, câimbras e tontura. Esses sintomas são resultado do ajuste eletrolítico e tendem a desaparecer espontaneamente. Hidratação adequada e suplementação de sódio, potássio e magnésio costumam aliviar os sintomas.</p>

