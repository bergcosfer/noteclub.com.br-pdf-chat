      <h1>Dieta Cetogênica<br><small style="font-size:.65em;color:#666">Guia Completo para Iniciantes — Edição 2024</small></h1>
      <p class="author">Dra. Fernanda Oliveira, Nutricionista CRN-3 · Instituto Brasileiro de Nutrição Funcional</p>

      <h2>O que é a Dieta Cetogênica?</h2>
      <p>A dieta cetogênica, popularmente conhecida como "dieta keto", é um plano alimentar de alto teor de gordura, proteína moderada e muito baixo carboidrato. O objetivo principal é levar o organismo a um estado metabólico denominado <strong>cetose</strong>, no qual o corpo passa a utilizar a gordura como principal fonte de energia em substituição aos carboidratos.</p>
      <p>Desenvolvida originalmente na década de 1920 para o tratamento de epilepsia refratária em crianças, a dieta cetogênica ganhou enorme popularidade nas últimas décadas como estratégia de perda de peso, melhora da sensibilidade à insulina e até como suporte terapêutico em algumas condições neurológicas e metabólicas.</p>

      <div class="highlight">
        💡 <strong>Fato importante:</strong> Na dieta cetogênica clássica, a proporção de macronutrientes é geralmente 70–75% de gorduras, 20–25% de proteínas e apenas 5% de carboidratos.
      </div>

      <h2>Como o Corpo Entra em Cetose?</h2>
      <p>Quando a ingestão de carboidratos é drasticamente reduzida (geralmente abaixo de 20–50 g por dia), os estoques de glicogênio hepático e muscular se esgotam. Em resposta, o fígado começa a converter ácidos graxos em corpos cetônicos — principalmente beta-hidroxibutirato, acetoacetato e acetona — que são exportados para outros tecidos como combustível alternativo.</p>

